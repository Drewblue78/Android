package com.example.project;

public class QuizSet {
    private float n;
    private float q;
    private float a;
    private float b;
    private float c;
    private float d;

    public QuizSet(float n, float q, float a, float b, float c, float d) {
        this.n = n;
        this.q = q;
        this.a = a;
        this.b = b;
        this.c = c;
        this.d = d;
    }

    public float getN() {
        return n;
    }

    public float getQ() {
        return q;
    }

    public float getA() {
        return a;
    }

    public float getB() {
        return b;
    }

    public float getC() {
        return c;
    }

    public float getD() {
        return d;
    }
}

